    /**
     * metodo para cuando clicamos recoge los datos que hay en las cajas las
     * compruba y pasa los datos a gestion de inscripciones para calificar
     *
     * @param evt
     */
    private void textBtCalifMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textBtCalifMouseClicked
        String telAlumno = cajaTelAlu.getText().trim();
        String nomCurso = cajaNombreCurso.getText().trim();
        String calilf = cajaCalif.getText().trim();
        try {
            float num = verif.esFloat(calilf);
            verif.hayAlgo(telAlumno);
            verif.soloNum(telAlumno);
            verif.nuveCaracteres(telAlumno);
            verif.hayAlgo(nomCurso);
            verif.tieneComa(telAlumno);
            verif.tieneComa(nomCurso);
            if (num >= 0) {
                gesInsc.CalificarAlumno(telAlumno, nomCurso, num);
                cajaTelAlu.setText("");
                cajaNombreCurso.setText("");
                cajaCalif.setText("");
                javax.swing.JOptionPane.showMessageDialog(this, "Calificado con exito", "MATRICULAR", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "No se puede calificar con negativos", "MATRICULAR", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (MisExceptions me) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + me, "MATRICULAR", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_textBtCalifMouseClicked

