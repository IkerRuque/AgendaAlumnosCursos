    /**
     * metodo para cuando clicamos recoge los datos que hay en las cajas las
     * compruba y pasa los datos a gestion de inscripciones para desmatricular
     *
     * @param evt
     */
    private void textBtDesMatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textBtDesMatMouseClicked
        String telAlumno = cajaTelAlu.getText().trim();
        String nomCurso = cajaNombreCurso.getText().trim();
        int n = JOptionPane.showConfirmDialog(
                this,
                "¿Estas seguro de querer desmatricular al alumno?",
                "Alumno desmatriculado",
                JOptionPane.YES_NO_OPTION);

        if (n == JOptionPane.OK_OPTION) {

            try {
                verif.hayAlgo(telAlumno);
                verif.soloNum(telAlumno);
                verif.nuveCaracteres(telAlumno);
                verif.hayAlgo(nomCurso);
                verif.tieneComa(telAlumno);
                verif.tieneComa(nomCurso);
                gesInsc.Desmatricular(telAlumno, nomCurso);
                cajaTelAlu.setText("");
                cajaNombreCurso.setText("");
                javax.swing.JOptionPane.showMessageDialog(this, "Desmatriculado con exito", "MATRICULAR", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            } catch (MisExceptions me) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + me, "MATRICULAR", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            }
        }
    }//GEN-LAST:event_textBtDesMatMouseClicked

